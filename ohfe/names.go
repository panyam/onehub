func RandomName() string {
	randAdj := rand.Intn(len(ADJECTIVES))
	randAnimal := rand.Intn(len(ANIMALS))

	adj := ADJECTIVES[randAdj]
	animal := ANIMALS[randAnimal]

	caser := cases.Title(language.English)
	return caser.String(adj + " " + animal)
}
